#define NKEYS (sizeof(lookuptable)/sizeof(pair))


void hdme_data_get_string(char *s, const char *key)
{
    size_t i;
    pair* item;
    int res = 0;

    for (i = 0; i < NKEYS; ++i)
      {
        item = &lookuptable[i];
        if (strcmp(item->key, key) == 0)
          {
            strcpy(s, item->val);
            res = 1;
            break;
          }
      }

    if (!res)
      {
        flint_printf("(hdme_data_get_string) Error: could not find key %s\n", key);
        fflush(stdout);
        flint_abort();
      }
}
